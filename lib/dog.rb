class Dog 
  def intitialize(name, breed)
    @name = name
    @breed = breed
  end
  
  def name()
    @name
  end
  
  def name=(name)
    @name = name
  end
  
  def breed()
    @breed
  end
  
  def breed=(breed)
    @breed = breed
  end

end #ends the Dog class

